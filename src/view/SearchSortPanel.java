    private void btnSortDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortDateActionPerformed
        ArrayList<Sale> sortedSales;

        if (SalesController.isAdmin()) {
            SalesController.sortByDate();
            sortedSales = SalesController.getAllSales();
        } else {
            sortedSales = SalesController.getSalesForCurrentUser();
            // Bubble sort by date (newest first)
            int n = sortedSales.size();
            for (int i = 0; i < n - 1; i++) {
                boolean swapped = false;
                for (int j = 0; j < n - i - 1; j++) {
                    if (sortedSales.get(j).getSaleDate().compareTo(sortedSales.get(j + 1).getSaleDate()) < 0) {
                        Sale temp = sortedSales.get(j);
                        sortedSales.set(j, sortedSales.get(j + 1));
                        sortedSales.set(j + 1, temp);
                        swapped = true;
                    }
                }
                if (!swapped) {
                    break;
                }
            }
        }

        mainFrame.showSalesList();
        mainFrame.passSearchResultsToSalesList(sortedSales, "Sorted by Date (Newest First)");
    }//GEN-LAST:event_btnSortDateActionPerformed

    private void btnSortQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortQuantityActionPerformed
        if (SalesController.isAdmin()) {
            SalesController.bubbleSortByQuantity();
        }

        ArrayList<Sale> sortedSales;
        if (SalesController.isAdmin()) {
            sortedSales = SalesController.getAllSales();
        } else {
            sortedSales = SalesController.getSalesForCurrentUser();
            // sort user's sales by quantity
            int n = sortedSales.size();
            for (int i = 0; i < n - 1; i++) {
                boolean swapped = false;
                for (int j = 0; j < n - i - 1; j++) {
                    if (sortedSales.get(j).getQuantity() > sortedSales.get(j + 1).getQuantity()) {
                        Sale temp = sortedSales.get(j);
                        sortedSales.set(j, sortedSales.get(j + 1));
                        sortedSales.set(j + 1, temp);
                        swapped = true;
                    }
                }
                if (!swapped) {
                    break;
                }
            }
        }

        mainFrame.showSalesList();
        mainFrame.passSearchResultsToSalesList(sortedSales, "Sorted by Quantity (Smallest to Largest)");
    }//GEN-LAST:event_btnSortQuantityActionPerformed

