    private void btnSearchStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchStatusActionPerformed
        String searchStatus = cmbSearchStatus.getSelectedItem().toString().trim();

        if (searchStatus.isEmpty() || searchStatus.equals("Select")) {
            JOptionPane.showMessageDialog(this,
                    "Please select a status",
                    "Selection Required",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        ArrayList<Sale> results;
        if (SalesController.isAdmin()) {
            results = new ArrayList<>();
            for (Sale sale : SalesController.getAllSales()) {
                if (sale.getStatus().equalsIgnoreCase(searchStatus)) {
                    results.add(sale);
                }
            }
        } else {
            results = SalesController.searchByStatusForCurrentUser(searchStatus);
        }

        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "No sales found with status: " + searchStatus,
                    "No Results",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        mainFrame.showSalesList();
        mainFrame.passSearchResultsToSalesList(results, "Status: " + searchStatus);
    }//GEN-LAST:event_btnSearchStatusActionPerformed

    private void btnSearchIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchIDActionPerformed
        String searchId = txtSearchID.getText().trim();
        if (searchId.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter an ID",
                    "Input Required",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Sale foundSale;
        if (SalesController.isAdmin()) {
            foundSale = SalesController.binarySearchById(searchId);
        } else {
            foundSale = SalesController.binarySearchByIdForCurrentUser(searchId);
        }

        if (foundSale == null) {
            JOptionPane.showMessageDialog(this,
                    "No sale found with ID: " + searchId,
                    "Not Found",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        ArrayList<Sale> results = new ArrayList<>();
        results.add(foundSale);

        mainFrame.showSalesList();
        mainFrame.passSearchResultsToSalesList(results, "Binary Search Results for ID: " + searchId);
    }//GEN-LAST:event_btnSearchIDActionPerformed

