    private void btnAddSalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSalesActionPerformed
        try {
            //Get values from form
            String name = txtName.getText().trim();
            String item = txtItem.getText().trim();
            String priceStr = txtPrice.getText().trim();
            String quantityStr = txtQuantity.getText().trim();
            String number = txtNumber.getText().trim();
            String dateStr = txtDate.getText().trim(); 
            String status = cmbStatus.getSelectedItem().toString();
            String paymentStatus = cmbPayment.getSelectedItem().toString();

            //Validate inputs
            if (name.isEmpty() || item.isEmpty() || priceStr.isEmpty()
                    || quantityStr.isEmpty() || number.isEmpty() || dateStr.isEmpty()) {        
                JOptionPane.showMessageDialog(this,
                        "All fields are required!",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Validate date format
            if (!dateStr.matches("\\d{4}-\\d{2}-\\d{2}")) {
                JOptionPane.showMessageDialog(this,
                        "Date must be in YYYY-MM-DD format (e.g., 2024-12-24)",
                        "Invalid Date Format",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Parse date and add current time
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date saleDate = sdf.parse(dateStr);

            // Add current time to the date
            java.util.Calendar cal = java.util.Calendar.getInstance();
            java.util.Calendar dateCal = java.util.Calendar.getInstance();
            dateCal.setTime(saleDate);
            dateCal.set(java.util.Calendar.HOUR_OF_DAY, cal.get(java.util.Calendar.HOUR_OF_DAY));
            dateCal.set(java.util.Calendar.MINUTE, cal.get(java.util.Calendar.MINUTE));
            dateCal.set(java.util.Calendar.SECOND, cal.get(java.util.Calendar.SECOND));
            saleDate = dateCal.getTime();

            //Validate date is not in future
            java.util.Date today = new java.util.Date();
            if (saleDate.after(today)) {
                JOptionPane.showMessageDialog(this,
                        "Sale date cannot be in the future!",
                        "Invalid Date",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Parse numbers
            double price = Double.parseDouble(priceStr);
            int quantity = Integer.parseInt(quantityStr);

            if (price <= 0) {
                JOptionPane.showMessageDialog(this,
                        "Price must be greater than 0",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this,
                        "Quantity must be greater than 0",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (number.length() < 10) {
                JOptionPane.showMessageDialog(this,
                        "Contact number must be at least 10 digits",
                        "Validation Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            //Generate ID and create Sale
            String id = SalesController.generateSaleId();
            Sale newSale = new Sale(id, name, item, price, quantity,
                    status, paymentStatus, saleDate, number);

            //Add to DataStore
            boolean success = SalesController.addSale(newSale);

            if (success) {
                // Show success message
                String message = "âœ… Sale added successfully!\n\n"
                        + "Sale ID: " + id + "\n"
                        + "Customer: " + name + "\n"
                        + "Item: " + item + "\n"
                        + "Total: $" + String.format("%.2f", price * quantity) + "\n"
                        + "Date: " + dateStr;

                JOptionPane.showMessageDialog(this,
                        message,
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);

                //Clear form
                btnClearActionPerformed(null);

            } else {
                JOptionPane.showMessageDialog(this,
                        "Failed to add sale. Please try again.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }

        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(this,
                    "Invalid date! Please use format: YYYY-MM-DD",
                    "Date Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Price and Quantity must be numbers!",
                    "Input Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddSalesActionPerformed

