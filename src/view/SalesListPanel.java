    private void btnUndoDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUndoDeleteActionPerformed
        Sale restoredSale = SalesController.undoDelete();

        if (restoredSale != null) {
            JOptionPane.showMessageDialog(this,
                    "Sale restored successfully!\n"
                    + "ID: " + restoredSale.getId() + "\n"
                    + "Customer: " + restoredSale.getCustomerName(),
                    "Undo Successful",
                    JOptionPane.INFORMATION_MESSAGE);

            loadSalesTable(); // Refresh the table
        } else {
            JOptionPane.showMessageDialog(this,
                    "No deleted sales to restore!",
                    "Undo Not Available",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUndoDeleteActionPerformed
    
