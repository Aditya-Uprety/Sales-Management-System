    /**
    * Loads sales data into the table based on user role.
    * Admins see all sales, regular users see only their own sales.
    */
    private void loadSalesTable() {
        try {
            java.util.ArrayList<model.Sale> salesToDisplay;

            if (currentDisplayList != null && !currentDisplayList.isEmpty()) {
                salesToDisplay = currentDisplayList;
            } else {
                // Regular view - filter by user
                if (controller.SalesController.isAdmin()) {
                    // Admin sees all sales
                    salesToDisplay = controller.SalesController.getAllSales();
                } else {
                    // Regular user sees only their sales
                    salesToDisplay = controller.SalesController.getSalesForCurrentUser();
                }
            }

            // Create table model
            String[] columns = {"ID", "Name", "Item", "Price", "Quantity", "Total",
                "Status", "Payment", "Contact", "Date"};
            DefaultTableModel model = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // Make table read-only
                }
            };

            // Add sales to table
            for (model.Sale sale : salesToDisplay) {
                Object[] row = {
                    sale.getId(),
                    sale.getCustomerName(),
                    sale.getItem(),
                    String.format("$%.2f", sale.getPrice()),
                    sale.getQuantity(),
                    String.format("$%.2f", sale.getTotal()),
                    sale.getStatus(),
                    sale.getPaymentStatus(),
                    sale.getContactNumber(),
                    sale.getSaleDate().toString().substring(0, 16)
                };
                model.addRow(row);
            }

            // Set model to table
            tblSalesList.setModel(model);

            // Auto-resize columns
            for (int i = 0; i < tblSalesList.getColumnCount(); i++) {
                tblSalesList.getColumnModel().getColumn(i).setPreferredWidth(120);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error loading sales: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

